#include "so_long.h"

void	pim_error(char *s, int mode)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	if (mode == 1)
		exit(EXIT_FAILURE);
}